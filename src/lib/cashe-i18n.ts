const BN_DIGITS = ["০", "১", "২", "৩", "৪", "৫", "৬", "৭", "৮", "৯"];

export function toBanglaDigits(value: string | number) {
  return String(value).replace(/\d/g, (d) => BN_DIGITS[Number(d)]);
}

export function bnText(en: string) {
  return BN[en] ?? en;
}

export function useCasheI18n() {
  const { lang, t } = useThemeLang();
  const isBn = lang === "bn";
  const tr = (en: string, bn?: string) => (isBn ? (bn ?? BN[en] ?? en) : en);
  const money = (cents: number) => {
    const formatted = "৳ " + (cents / 100).toLocaleString(isBn ? "bn-BD" : "en-BD", { maximumFractionDigits: 2 });
    return formatted;
  };
  const num = (value: string | number) => (isBn ? toBanglaDigits(value) : String(value));
  return { lang, isBn, tr, t, money, num };
}
